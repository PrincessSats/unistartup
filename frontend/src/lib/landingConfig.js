export function normalizeHashPath(hashValue) {
  const raw = String(hashValue || '').trim();
  if (!raw) return '/';
  const withoutHash = raw.startsWith('#') ? raw.slice(1) : raw;
  const [path] = withoutHash.split('?');
  if (!path) return '/';
  return path.startsWith('/') ? path : `/${path}`;
}

export function isLandingPublicPath(pathname) {
  const normalizedPath = String(pathname || '').trim();
  return (
    normalizedPath === LANDING_ROUTE_PREFIX ||
    normalizedPath.startsWith(`${LANDING_ROUTE_PREFIX}/legal/`)
  );
}

export function isLandingPublicHash(hashValue) {
  return isLandingPublicPath(normalizeHashPath(hashValue));
}
